    private void editarBlocoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBlocoActionPerformed
        String nome = descricao.getText();
        String bloc = bloco.getSelectedItem().toString();
        Integer capacidadeSala = (Integer) capacidade.getValue();
        String tipoSala = tipo.getSelectedItem().toString();;

        if( validarCampos()){
            try {
                fachadaAssistente.editarSala(nome, bloc, capacidadeSala, tipoSala);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarMaterial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if(tela == "inicial") {

            try {
                this.telaInicial.carregarJTableSala();
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarMaterial.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else{
            try {
                this.telaAssistente.carregarJTableSala();
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarMaterial.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_editarBlocoActionPerformed

