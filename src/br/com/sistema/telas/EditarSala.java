    public boolean validarCampos(){
        Integer capacidadeSala = (Integer) capacidade.getValue();
            
            if(descricao.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Digite a descrição!");
                descricao.requestFocus();
                return false;
            }
           if(capacidadeSala <= 0){
               JOptionPane.showMessageDialog(null, "Selecione um número maior que zero!");
                capacidade.requestFocus();
                return false;
           }
           
        return true;
    }
    
    public void recebeParametros(List lista) throws SQLException {
        descricao.setText((String) lista.get(0));
        bloco.getModel().setSelectedItem((String) lista.get(1));
        capacidade.setValue(lista.get(2));
        tipo.setSelectedItem((String) lista.get(3));
    }
    
    public void carregarTexto(boolean b){
        descricao.setEnabled(!b);
        capacidade.setEnabled(!b);
        bloco.setEnabled(!b);
        tipo.setEnabled(!b);
        
        Editar.setEnabled(b);
    }
    
